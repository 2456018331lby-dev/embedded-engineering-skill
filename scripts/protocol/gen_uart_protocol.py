@dataclass
class UARTProtoOutput:
    success:         bool
    inputs:          Dict[str, Any]
    results:         Dict[str, Any]
    warnings:        List[str]
    recommendations: List[str]
    next_actions:    List[str]
    error:           Optional[Dict[str, str]] = None


def gen_uart_protocol(
    name:              str,
    baud:              int               = 115200,
    frame_header_hex:  str               = "AA 55",
    crc_type:          str               = "CRC8",
    commands_str:      str               = "READ:0x01,WRITE:0x02,ACK:0x10",
    max_payload_bytes: int               = 64,
) -> Dict[str, Any]:
    """
    Generate a UART custom protocol definition with C and Python code.

    Parameters
    ----------
    name              : Protocol/module name, e.g. "SensorProtocol"
    baud              : UART baud rate
    frame_header_hex  : Sync header bytes, e.g. "AA 55"
    crc_type          : CRC algorithm (CRC8 / CRC16_MODBUS / CRC16_CCITT / CRC32)
    commands_str      : Comma-separated commands: "NAME:0xCODE[:payload_len[:dir]]"
    max_payload_bytes : Maximum payload size per frame
    """
    inputs: Dict[str, Any] = {
        "name": name, "baud": baud,
        "frame_header_hex": frame_header_hex,
        "crc_type": crc_type, "commands_str": commands_str,
        "max_payload_bytes": max_payload_bytes,
    }

    try:
        from gen_crc_frame import ALGOS
    except ImportError:
        ALGOS_KEYS = ["CRC8", "CRC8_MAXIM", "CRC16_MODBUS", "CRC16_CCITT", "CRC16_IBM", "CRC32"]
        if crc_type.upper() not in ALGOS_KEYS:
            pass  # will catch below

    try:
        crc_key = crc_type.upper().strip()
        if crc_key not in _CRC_TYPE_MAP:
            raise ValueError(
                f"Unsupported CRC type '{crc_type}'. "
                f"Supported: {', '.join(_CRC_TYPE_MAP)}"
            )
        if max_payload_bytes < 1 or max_payload_bytes > 512:
            raise ValueError("max_payload_bytes must be between 1 and 512.")

        proto_name   = re.sub(r"\W", "_", name.strip())
        header_bytes = bytes(int(t, 16) for t in frame_header_hex.split() if t)
        if len(header_bytes) < 2:
            raise ValueError("frame_header_hex must contain at least 2 bytes.")

        commands = _parse_commands(commands_str)
        if not commands:
            raise ValueError("At least one command must be specified.")

        # Check for duplicate command codes
        codes_seen: Dict[int, str] = {}
        for c in commands:
            if c.code in codes_seen:
                raise ValueError(
                    f"Duplicate command code 0x{c.code:02X} "
                    f"({codes_seen[c.code]} and {c.name})."
                )
            codes_seen[c.code] = c.name

        crc_size = _CRC_TYPE_MAP[crc_key][1]
        max_frame = 2 + 1 + 1 + max_payload_bytes + crc_size

        h_code  = _gen_header(proto_name, header_bytes, baud, commands, crc_key, max_payload_bytes)
        c_code  = _gen_source(proto_name, header_bytes, commands, crc_key, max_payload_bytes)
        py_code = _gen_python_ref(proto_name, header_bytes, baud, commands, crc_key)

        frame_structure = {
            "byte_0":      f"0x{header_bytes[0]:02X} (header sync 0)",
            "byte_1":      f"0x{header_bytes[1]:02X} (header sync 1)",
            "byte_2":      "LEN  — payload byte count (0–" + str(max_payload_bytes) + ")",
            "byte_3":      "CMD  — command code",
            "byte_4_to_N": "PAYLOAD (0–" + str(max_payload_bytes) + " bytes)",
            "last_bytes":  f"CRC  ({crc_size} byte{'s' if crc_size > 1 else ''}, {crc_key})",
            "crc_coverage": "[LEN][CMD][PAYLOAD]",
            "max_frame_bytes": max_frame,
        }

        cmd_table = [
            {"name": c.name, "code": f"0x{c.code:02X}",
             "direction": c.direction, "payload_len": c.payload_len,
             "description": c.description}
            for c in commands
        ]

        warnings: List[str] = []
        if baud > 921600:
            warnings.append(
                f"Baud rate {baud} is very high; verify UART clock accuracy "
                "and cable/line capacitance."
            )
        if max_payload_bytes > 128:
            warnings.append(
                f"MAX_PAYLOAD = {max_payload_bytes} bytes; ensure the receiving MCU "
                "has sufficient RAM for the frame buffer."
            )
        if crc_key == "CRC8" and max_payload_bytes > 16:
            warnings.append(
                "CRC8 with large payloads has limited error-detection capability. "
                "Consider CRC16_MODBUS or CRC32 for payloads > 16 bytes."
            )

        recs: List[str] = [
            "Copy gen_crc_frame.py output for the CRC function into a "
            "separate crc_utils.c/h file and link it with the generated source.",
            "Use DMA + UART IDLE-line interrupt (STM32 HAL_UARTEx_ReceiveToIdle_DMA) "
            "for efficient non-blocking reception on STM32.",
            "On ESP32, use uart_driver_install() with a ring buffer; "
            "process frames in a dedicated FreeRTOS task.",
            "Add a sequence number or timestamp field to the payload if out-of-order "
            "or dropped-packet detection is required.",
        ]

        return asdict(UARTProtoOutput(
            success=True, inputs=inputs,
            results={
                "frame_structure":   frame_structure,
                "command_table":     cmd_table,
                "c_header":          h_code,
                "c_source":          c_code,
                "python_reference":  py_code,
                "files_to_create":   [
                    f"{proto_name}.h  (copy c_header)",
                    f"{proto_name}.c  (copy c_source, implement crc function)",
                    f"{proto_name}_host.py  (copy python_reference)",
                ],
            },
            warnings=warnings, recommendations=recs,
            next_actions=["gen_firmware_skeleton", "gen_crc_frame"],
        ))

    except Exception as exc:
        return asdict(UARTProtoOutput(
            success=False, inputs=inputs, results={},
            warnings=[], recommendations=[], next_actions=[],
            error={"code": exc.__class__.__name__.upper(), "message": str(exc)},
        ))


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main() -> None:
    p = argparse.ArgumentParser(
        description="UART protocol frame definition and C/Python code generator",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument("--name",        required=True,  help="Protocol/module name")
    p.add_argument("--baud",        type=int, default=115200)
    p.add_argument("--frame-header", default="AA 55", dest="frame_header_hex")
    p.add_argument("--crc",          default="CRC8", dest="crc_type",
                   choices=list(_CRC_TYPE_MAP))
    p.add_argument("--commands",    required=True, dest="commands_str",
                   help="CMD1:0xCODE,CMD2:0xCODE  or  CMD:0xCODE:payload_len:direction")
    p.add_argument("--max-payload", type=int, default=64, dest="max_payload_bytes")
    args = p.parse_args()

    result = gen_uart_protocol(
        name=args.name, baud=args.baud,
        frame_header_hex=args.frame_header_hex,
        crc_type=args.crc_type, commands_str=args.commands_str,
        max_payload_bytes=args.max_payload_bytes,
    )
    print(json.dumps(result, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
