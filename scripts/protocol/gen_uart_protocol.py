#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
gen_uart_protocol.py

UART custom protocol frame definition and C firmware code generator
for embedded communication workflows.

Given a protocol name, baud rate, frame header, CRC type, and a list of
command definitions, this script generates:

- Validated frame structure description (field widths, byte budget)
- Complete C header file (.h): enums, frame struct, function prototypes
- Complete C source file (.c): pack / unpack / send / receive skeleton
- Python reference implementation for host-side testing
- Unified dict/JSON output contract

Frame format (fixed):
  [HEADER_0][HEADER_1][LEN][CMD][PAYLOAD...][CRC]

LEN  = number of PAYLOAD bytes (not counting CMD or CRC)
CRC  = computed over [LEN][CMD][PAYLOAD]
"""

from __future__ import annotations

import argparse
import json
import re
from dataclasses import asdict, dataclass
from textwrap import dedent
from typing import Any, Dict, List, Optional, Tuple


# ---------------------------------------------------------------------------
# Command descriptor
# ---------------------------------------------------------------------------

@dataclass
class Command:
    name:     str    # e.g. "READ_SENSOR"
    code:     int    # e.g. 0x01
    direction: str   # "host_to_device" | "device_to_host" | "bidirectional"
    payload_len: int # fixed payload byte count, -1 = variable
    description: str


def _parse_commands(raw: str) -> List[Command]:
    """
    Parse command string like:
      "READ_SENSOR:0x01,SET_CONFIG:0x02,ACK:0x10"
    or extended JSON-like:
      "READ_SENSOR:0x01:4:host_to_device,ACK:0x10:0:device_to_host"
    """
    cmds: List[Command] = []
    for part in raw.split(","):
        part = part.strip()
        if not part:
            continue
        fields = part.split(":")
        name  = fields[0].strip().upper().replace(" ", "_")
        code  = int(fields[1].strip(), 16) if len(fields) > 1 else 0x00
        plen  = int(fields[2].strip()) if len(fields) > 2 else -1
        direc = fields[3].strip() if len(fields) > 3 else "bidirectional"
        desc  = fields[4].strip() if len(fields) > 4 else ""
        cmds.append(Command(name=name, code=code, direction=direc,
                            payload_len=plen, description=desc))
    return cmds


# ---------------------------------------------------------------------------
# C code generation
# ---------------------------------------------------------------------------

_CRC_TYPE_MAP = {
    "CRC8":         ("uint8_t",  1, "calc_crc8"),
    "CRC8_MAXIM":   ("uint8_t",  1, "calc_crc8_maxim"),
    "CRC16_MODBUS": ("uint16_t", 2, "calc_crc16_modbus"),
    "CRC16_CCITT":  ("uint16_t", 2, "calc_crc16_ccitt"),
    "CRC32":        ("uint32_t", 4, "calc_crc32"),
}


def _gen_header(
    proto_name: str,
    header_bytes: bytes,
    baud: int,
    commands: List[Command],
    crc_type: str,
    max_payload: int,
) -> str:
    guard  = re.sub(r"\W", "_", proto_name).upper() + "_H"
    ctype, crc_size, crc_fn = _CRC_TYPE_MAP.get(crc_type, ("uint8_t", 1, "calc_crc8"))
    header_hex = ", ".join(f"0x{b:02X}" for b in header_bytes)
    cmd_enum   = "\n".join(
        f"    CMD_{c.name:<24} = 0x{c.code:02X}U,  /* {c.description or c.direction} */"
        for c in commands
    )
    max_frame = 2 + 1 + 1 + max_payload + crc_size   # header + len + cmd + payload + crc

    return dedent(f"""\
        /* Auto-generated by gen_uart_protocol.py */
        #ifndef {guard}
        #define {guard}

        #include <stdint.h>
        #include <stddef.h>
        #include <stdbool.h>

        /* ---- Protocol constants ---- */
        #define {proto_name.upper()}_BAUD_RATE     {baud}U
        #define {proto_name.upper()}_HEADER_0      {f"0x{header_bytes[0]:02X}U" if header_bytes else "0xAAU"}
        #define {proto_name.upper()}_HEADER_1      {f"0x{header_bytes[1]:02X}U" if len(header_bytes) > 1 else "0x55U"}
        #define {proto_name.upper()}_MAX_PAYLOAD   {max_payload}U
        #define {proto_name.upper()}_MAX_FRAME     {max_frame}U
        #define {proto_name.upper()}_CRC_SIZE      {crc_size}U

        /* ---- Command codes ---- */
        typedef enum {{
        {cmd_enum}
        }} {proto_name}_cmd_t;

        /* ---- Frame state machine ---- */
        typedef enum {{
            FRAME_STATE_IDLE,
            FRAME_STATE_HEADER1,
            FRAME_STATE_LEN,
            FRAME_STATE_CMD,
            FRAME_STATE_PAYLOAD,
            FRAME_STATE_CRC,
        }} {proto_name}_rx_state_t;

        /* ---- Frame context ---- */
        typedef struct {{
            {proto_name}_rx_state_t state;
            uint8_t  len;
            uint8_t  cmd;
            uint8_t  payload[{proto_name.upper()}_MAX_PAYLOAD];
            uint16_t payload_idx;
            {ctype}  crc_received;
            uint8_t  crc_byte_idx;
        }} {proto_name}_rx_ctx_t;

        /* ---- Public API ---- */
        /* Pack a frame into buf. Returns frame length in bytes. */
        uint16_t {proto_name}_pack(uint8_t cmd, const uint8_t *payload, uint8_t plen,
                                    uint8_t *buf, uint16_t buf_size);

        /* Feed one received byte into the state machine.
           Returns true when a complete, CRC-valid frame has been received. */
        bool {proto_name}_feed_byte(uint8_t byte, {proto_name}_rx_ctx_t *ctx);

        /* Reset receiver context (call after error or frame handled). */
        void {proto_name}_reset_ctx({proto_name}_rx_ctx_t *ctx);

        /* CRC function — implement using gen_crc_frame.py output */
        {ctype} {crc_fn}(const uint8_t *data, size_t len);

        #endif /* {guard} */
        """)


def _gen_source(
    proto_name: str,
    header_bytes: bytes,
    commands: List[Command],
    crc_type: str,
    max_payload: int,
) -> str:
    ctype, crc_size, crc_fn = _CRC_TYPE_MAP.get(crc_type, ("uint8_t", 1, "calc_crc8"))
    pname_up = proto_name.upper()

    crc_pack = ""
    if crc_size == 1:
        crc_pack = dedent("""\
            buf[pos++] = (uint8_t)crc;
            """)
    elif crc_size == 2:
        crc_pack = dedent("""\
            buf[pos++] = (uint8_t)(crc >> 8);   /* big-endian */
            buf[pos++] = (uint8_t)(crc & 0xFF);
            """)
    else:
        crc_pack = dedent("""\
            buf[pos++] = (uint8_t)(crc >> 24);
            buf[pos++] = (uint8_t)(crc >> 16);
            buf[pos++] = (uint8_t)(crc >> 8);
            buf[pos++] = (uint8_t)(crc & 0xFF);
            """)

    crc_accum = ""
    if crc_size == 1:
        crc_accum = "ctx->crc_received = byte;"
    elif crc_size == 2:
        crc_accum = dedent("""\
            if (ctx->crc_byte_idx == 0) ctx->crc_received  = (uint16_t)byte << 8;
            else                        ctx->crc_received |= byte;
            """).strip()
    else:
        crc_accum = dedent("""\
            ctx->crc_received = (ctx->crc_received << 8) | byte;
            """).strip()

    return dedent(f"""\
        /* Auto-generated by gen_uart_protocol.py */
        #include "{proto_name}.h"
        #include <string.h>

        /* ---- Frame packer ---- */
        uint16_t {proto_name}_pack(uint8_t cmd, const uint8_t *payload, uint8_t plen,
                                    uint8_t *buf, uint16_t buf_size)
        {{
            if (buf_size < (uint16_t)(2U + 1U + 1U + plen + {pname_up}_CRC_SIZE))
                return 0U;

            uint16_t pos = 0;
            buf[pos++] = {pname_up}_HEADER_0;
            buf[pos++] = {pname_up}_HEADER_1;
            buf[pos++] = plen;                      /* LEN = payload bytes only */
            buf[pos++] = cmd;

            if (payload && plen)
                memcpy(&buf[pos], payload, plen);
            pos += plen;

            /* CRC covers [LEN][CMD][PAYLOAD] */
            {ctype} crc = {crc_fn}(&buf[2], (size_t)(2U + plen));
            {crc_pack}
            return pos;
        }}

        /* ---- Receiver context reset ---- */
        void {proto_name}_reset_ctx({proto_name}_rx_ctx_t *ctx)
        {{
            memset(ctx, 0, sizeof(*ctx));
            ctx->state = FRAME_STATE_IDLE;
        }}

        /* ---- Byte-feed state machine ---- */
        bool {proto_name}_feed_byte(uint8_t byte, {proto_name}_rx_ctx_t *ctx)
        {{
            switch (ctx->state) {{

                case FRAME_STATE_IDLE:
                    if (byte == {pname_up}_HEADER_0)
                        ctx->state = FRAME_STATE_HEADER1;
                    break;

                case FRAME_STATE_HEADER1:
                    ctx->state = (byte == {pname_up}_HEADER_1)
                                 ? FRAME_STATE_LEN : FRAME_STATE_IDLE;
                    break;

                case FRAME_STATE_LEN:
                    if (byte > {pname_up}_MAX_PAYLOAD) {{
                        {proto_name}_reset_ctx(ctx);   /* oversized frame */
                        break;
                    }}
                    ctx->len         = byte;
                    ctx->payload_idx = 0;
                    ctx->state       = FRAME_STATE_CMD;
                    break;

                case FRAME_STATE_CMD:
                    ctx->cmd   = byte;
                    ctx->state = (ctx->len > 0) ? FRAME_STATE_PAYLOAD : FRAME_STATE_CRC;
                    ctx->crc_byte_idx = 0;
                    break;

                case FRAME_STATE_PAYLOAD:
                    ctx->payload[ctx->payload_idx++] = byte;
                    if (ctx->payload_idx >= ctx->len) {{
                        ctx->state        = FRAME_STATE_CRC;
                        ctx->crc_byte_idx = 0;
                    }}
                    break;

                case FRAME_STATE_CRC: {{
                    {crc_accum}
                    ctx->crc_byte_idx++;
                    if (ctx->crc_byte_idx >= {pname_up}_CRC_SIZE) {{
                        /* Recompute CRC over [LEN][CMD][PAYLOAD] */
                        uint8_t tmp[2 + {pname_up}_MAX_PAYLOAD];
                        tmp[0] = ctx->len;
                        tmp[1] = ctx->cmd;
                        if (ctx->len) memcpy(&tmp[2], ctx->payload, ctx->len);
                        {ctype} expected = {crc_fn}(tmp, (size_t)(2U + ctx->len));
                        bool valid = (expected == ctx->crc_received);
                        {proto_name}_reset_ctx(ctx);
                        return valid;
                    }}
                    break;
                }}

                default:
                    {proto_name}_reset_ctx(ctx);
                    break;
            }}
            return false;
        }}
        """)


def _gen_python_ref(
    proto_name: str,
    header_bytes: bytes,
    baud: int,
    commands: List[Command],
    crc_type: str,
) -> str:
    hdr0 = header_bytes[0] if header_bytes else 0xAA
    hdr1 = header_bytes[1] if len(header_bytes) > 1 else 0x55

    cmd_consts = "\n".join(
        f"    {c.name:<24} = 0x{c.code:02X}" for c in commands
    )

    return dedent(f"""\
        \"\"\"
        {proto_name} — Python reference implementation (host side)
        Auto-generated by gen_uart_protocol.py
        \"\"\"
        import struct
        import serial
        from gen_crc_frame import gen_crc_frame, ALGOS, _compute_crc, _parse_hex

        BAUD   = {baud}
        HDR_0  = 0x{hdr0:02X}
        HDR_1  = 0x{hdr1:02X}
        CRC_ALGO = "{crc_type}"

        class Cmd:
        {cmd_consts}


        def _crc(data: bytes) -> bytes:
            algo = ALGOS[CRC_ALGO]
            val  = _compute_crc(data, algo)
            return val.to_bytes(algo.width // 8, "big")


        def pack(cmd: int, payload: bytes = b"") -> bytes:
            \"\"\"Build a protocol frame.\"\"\"
            body = bytes([len(payload), cmd]) + payload
            return bytes([HDR_0, HDR_1]) + body + _crc(body)


        def send(port: serial.Serial, cmd: int, payload: bytes = b"") -> None:
            frame = pack(cmd, payload)
            port.write(frame)


        def receive(port: serial.Serial, timeout: float = 1.0) -> dict | None:
            \"\"\"
            Blocking receive.  Returns dict(cmd, payload) or None on timeout/error.
            \"\"\"
            port.timeout = timeout
            algo    = ALGOS[CRC_ALGO]
            crc_len = algo.width // 8
            buf     = bytearray()
            state   = "IDLE"
            plen    = 0

            while True:
                b = port.read(1)
                if not b:
                    return None
                byte = b[0]

                if state == "IDLE":
                    if byte == HDR_0: state = "HDR1"
                elif state == "HDR1":
                    state = "LEN" if byte == HDR_1 else "IDLE"
                elif state == "LEN":
                    plen  = byte
                    buf   = bytearray([plen])
                    state = "CMD"
                elif state == "CMD":
                    cmd_byte = byte
                    buf.append(byte)
                    state = "PAYLOAD" if plen else "CRC"
                elif state == "PAYLOAD":
                    buf.append(byte)
                    if len(buf) >= plen + 2:   # len byte + cmd + payload
                        state = "CRC"
                elif state == "CRC":
                    buf.append(byte)
                    if len(buf) >= plen + 2 + crc_len:
                        payload_data = bytes(buf[2:-crc_len])
                        rx_crc       = int.from_bytes(buf[-crc_len:], "big")
                        expected     = _compute_crc(bytes(buf[:-crc_len]), algo)
                        if rx_crc == expected:
                            return {{"cmd": cmd_byte, "payload": payload_data}}
                        return None   # CRC mismatch


        if __name__ == "__main__":
            import sys
            port = serial.Serial(sys.argv[1] if len(sys.argv) > 1 else "/dev/ttyUSB0",
                                 BAUD, timeout=1)
            print("Sending READ_SENSOR command...")
            send(port, Cmd.{"READ_SENSOR" if any(c.name == "READ_SENSOR" for c in commands) else commands[0].name if commands else "CMD_0x01"})
            resp = receive(port)
            print("Response:", resp)
        """)


