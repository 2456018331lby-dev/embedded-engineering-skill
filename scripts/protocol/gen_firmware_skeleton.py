def _esp32_main(project: str, peripherals: List[str], rtos: str) -> str:
    inits = []
    if "uart" in peripherals:
        inits.append(dedent("""\
            /* UART init */
            uart_config_t uart_cfg = {
                .baud_rate  = 115200,
                .data_bits  = UART_DATA_8_BITS,
                .parity     = UART_PARITY_DISABLE,
                .stop_bits  = UART_STOP_BITS_1,
                .flow_ctrl  = UART_HW_FLOWCTRL_DISABLE,
            };
            uart_driver_install(UART_NUM_1, 1024, 0, 0, NULL, 0);
            uart_param_config(UART_NUM_1, &uart_cfg);"""))
    if "wifi" in peripherals:
        inits.append(dedent("""\
            /* Wi-Fi init */
            esp_netif_init();
            esp_event_loop_create_default();
            esp_netif_create_default_wifi_sta();
            wifi_init_config_t wifi_cfg = WIFI_INIT_CONFIG_DEFAULT();
            esp_wifi_init(&wifi_cfg);"""))
    if "ble" in peripherals:
        inits.append('esp_bt_controller_config_t bt_cfg = BT_CONTROLLER_INIT_CONFIG_DEFAULT();\n    esp_bt_controller_init(&bt_cfg);')

    init_block = "\n    ".join(inits) if inits else "/* No peripherals configured */"

    task_fn = ""
    if rtos == "freertos":
        task_fn = dedent("""\

            static void app_task(void *arg) {
                for (;;) {
                    /* TODO: application logic */
                    vTaskDelay(pdMS_TO_TICKS(100));
                }
            }
            """)

    task_create = ""
    if rtos == "freertos":
        task_create = '\n    xTaskCreate(app_task, "app", 4096, NULL, 5, NULL);'

    includes = '#include "esp_log.h"\n#include "driver/uart.h"'
    if "wifi" in peripherals:
        includes += '\n#include "esp_wifi.h"\n#include "esp_netif.h"'
    if "ble" in peripherals:
        includes += '\n#include "esp_bt.h"'

    return dedent(f"""\
        /* {project} — ESP32 main entry point */
        /* Auto-generated by gen_firmware_skeleton.py  */
        #include <stdio.h>
        {includes}
        #include "freertos/FreeRTOS.h"
        #include "freertos/task.h"

        static const char *TAG = "{project}";
        {task_fn}
        void app_main(void) {{
            ESP_LOGI(TAG, "{project} starting...");

            {init_block}
            {task_create}
            ESP_LOGI(TAG, "Init complete.");
        }}
        """)


def _esp32_cmakelists(project: str) -> str:
    return dedent(f"""\
        cmake_minimum_required(VERSION 3.16)
        include($ENV{{IDF_PATH}}/tools/cmake/project.cmake)
        project({project.lower()})
        """)


def _esp32_component_cmake(project: str) -> str:
    return dedent(f"""\
        idf_component_register(
            SRCS "main.c"
            INCLUDE_DIRS "."
        )
        """)


def _esp32_kconfig(project: str) -> str:
    return dedent(f"""\
        menu "{project} Configuration"

        config {project.upper()}_LOG_LEVEL
            int "Log verbosity level (0=None ... 5=Verbose)"
            default 3

        config {project.upper()}_UART_BAUD
            int "UART baud rate"
            default 115200

        endmenu
        """)


def _esp32_readme(project: str, peripherals: List[str]) -> str:
    return dedent(f"""\
        # {project}

        Platform : ESP32 (ESP-IDF)
        Peripherals: {', '.join(peripherals) or 'none selected'}
        Generated: gen_firmware_skeleton.py

        ## Prerequisites
        - ESP-IDF v5.x installed and `idf.py` on PATH
        - `IDF_PATH` environment variable set

        ## Build and flash
        ```bash
        idf.py set-target esp32
        idf.py menuconfig      # optional: tweak Kconfig options
        idf.py build
        idf.py -p /dev/ttyUSB0 flash monitor
        ```

        ## Project structure
        ```
        {project}/
        ├── main/
        │   ├── CMakeLists.txt
        │   ├── Kconfig.projbuild
        │   └── main.c
        ├── CMakeLists.txt
        └── README.md
        ```

        ## Next steps
        1. Implement TODO sections in main.c.
        2. Add components for custom drivers under components/.
        3. Integrate gen_uart_protocol.py generated files for communication.
        """)


