#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
gen_firmware_skeleton.py

Firmware project skeleton generator for embedded systems.

Supported platforms
  stm32   : STM32 HAL-based project (CMakeLists or Makefile scaffold)
  esp32   : ESP32 ESP-IDF project (CMakeLists + Kconfig scaffold)
  arduino : Arduino sketch project

For each platform the script generates a complete, compilable starting
structure: directory layout, main source file, peripheral driver stubs,
FreeRTOS task template (if requested), and a README with build instructions.

Output is a JSON dict containing every file path and its content as a string.
The user copies these files into their project directory.

Unified dict/JSON output contract.
"""

from __future__ import annotations

import argparse
import json
from dataclasses import asdict, dataclass
from textwrap import dedent
from typing import Any, Dict, List, Optional

PLATFORMS  = ("stm32", "esp32", "arduino")
STM32_SERIES = ("F0", "F1", "F4", "G0", "G4", "H7")
PERIPHERALS  = ("uart", "spi", "i2c", "tim", "adc", "dac", "wifi", "ble", "can", "usb")
RTOS_OPTIONS = ("none", "freertos", "zephyr")


# ---------------------------------------------------------------------------
# STM32 HAL skeleton
# ---------------------------------------------------------------------------

def _stm32_main(project: str, series: str, peripherals: List[str], rtos: str) -> str:
    inits = []
    if "uart" in peripherals: inits.append("MX_USART2_UART_Init();")
    if "spi"  in peripherals: inits.append("MX_SPI1_Init();")
    if "i2c"  in peripherals: inits.append("MX_I2C1_Init();")
    if "tim"  in peripherals: inits.append("MX_TIM2_Init();")
    if "adc"  in peripherals: inits.append("MX_ADC1_Init();")
    if "dac"  in peripherals: inits.append("MX_DAC_Init();")
    if "can"  in peripherals: inits.append("MX_CAN1_Init();")
    if "usb"  in peripherals: inits.append("MX_USB_DEVICE_Init();")
    init_block = "\n    ".join(inits) if inits else "/* No peripherals selected */"

    rtos_block = ""
    if rtos == "freertos":
        rtos_block = dedent("""\

            /* FreeRTOS tasks */
            #include "FreeRTOS.h"
            #include "task.h"

            static void app_task(void *arg) {
                (void)arg;
                for (;;) {
                    /* TODO: application logic */
                    vTaskDelay(pdMS_TO_TICKS(100));
                }
            }
            """)

    main_body = dedent(f"""\
        /* {project} — STM32{series} main entry point */
        /* Auto-generated by gen_firmware_skeleton.py  */
        /* Platform: STM32{series} | Peripherals: {', '.join(peripherals) or 'none'} */

        #include "main.h"
        {rtos_block}
        int main(void) {{
            HAL_Init();
            SystemClock_Config();

            /* Peripheral init (generated by CubeMX — replace with your MX_ functions) */
            {init_block}
        """)

    if rtos == "freertos":
        main_body += dedent("""\

                xTaskCreate(app_task, "AppTask", 256, NULL, tskIDLE_PRIORITY + 1, NULL);
                vTaskStartScheduler();
                /* Should never reach here */
                for (;;);
            }
            """)
    else:
        main_body += dedent("""\

                while (1) {
                    /* TODO: main loop */
                    HAL_Delay(10);
                }
            }
            """)

    return main_body


def _stm32_main_h(project: str, series: str) -> str:
    return dedent(f"""\
        /* {project} main.h — STM32{series} */
        #ifndef MAIN_H
        #define MAIN_H

        #include "stm32{series.lower()}xx_hal.h"

        void SystemClock_Config(void);
        void Error_Handler(void);

        #endif /* MAIN_H */
        """)


def _stm32_peripheral_stub(periph: str) -> str:
    periph_up = periph.upper()
    handles = {
        "uart": "extern UART_HandleTypeDef huart2;",
        "spi":  "extern SPI_HandleTypeDef hspi1;",
        "i2c":  "extern I2C_HandleTypeDef hi2c1;",
        "tim":  "extern TIM_HandleTypeDef htim2;",
        "adc":  "extern ADC_HandleTypeDef hadc1;",
        "dac":  "extern DAC_HandleTypeDef hdac;",
        "can":  "extern CAN_HandleTypeDef hcan1;",
    }
    handle_line = handles.get(periph, f"/* {periph_up} handle */" )
    return dedent(f"""\
        /* {periph_up} driver stub — auto-generated */
        #include "main.h"

        {handle_line}

        void {periph}_driver_init(void) {{
            /* TODO: additional {periph_up} configuration beyond CubeMX defaults */
        }}

        void {periph}_driver_process(void) {{
            /* TODO: {periph_up} polling / callback logic */
        }}
        """)


def _stm32_makefile(project: str, series: str) -> str:
    return dedent(f"""\
        # {project} — STM32{series} build scaffold
        # Adapt paths to your STM32CubeF{series[1:]}/G{series[1:]} SDK location

        TARGET   = {project.lower()}
        SERIES   = STM32{series}xx
        CPU      = -mcpu=cortex-m4 -mthumb -mfpu=fpv4-sp-d16 -mfloat-abi=hard

        CC       = arm-none-eabi-gcc
        CFLAGS   = $(CPU) -Wall -O2 -g -DUSE_HAL_DRIVER -D$(SERIES)
        LDFLAGS  = $(CPU) -specs=nano.specs -Wl,--gc-sections

        SRC      = $(wildcard Core/Src/*.c)
        INC      = -ICore/Inc -IDrivers/STM32$(SERIES)_HAL_Driver/Inc

        all:
        \t$(CC) $(CFLAGS) $(INC) $(SRC) $(LDFLAGS) -o $(TARGET).elf
        \tarm-none-eabi-objcopy -O binary $(TARGET).elf $(TARGET).bin
        \tarm-none-eabi-size $(TARGET).elf

        flash:
        \tst-flash write $(TARGET).bin 0x8000000

        clean:
        \trm -f $(TARGET).elf $(TARGET).bin
        """)


def _stm32_readme(project: str, series: str, peripherals: List[str], rtos: str) -> str:
    return dedent(f"""\
        # {project}

        Platform : STM32{series}
        Peripherals: {', '.join(peripherals) or 'none selected'}
        RTOS     : {rtos}
        Generated: gen_firmware_skeleton.py

        ## Build

        ### Option A — STM32CubeIDE (recommended)
        1. File → Import → Existing Projects into Workspace
        2. Select this directory
        3. Build Project (Ctrl+B)
        4. Run → Debug (F11)

        ### Option B — arm-none-eabi-gcc + make
        ```bash
        make all
        make flash   # requires st-link
        ```

        ## Project structure
        ```
        {project}/
        ├── Core/
        │   ├── Inc/main.h
        │   └── Src/main.c
        ├── Drivers/          ← add STM32CubeF{series[1:]} HAL drivers here
        ├── Peripherals/      ← generated driver stubs
        ├── Makefile
        └── README.md
        ```

        ## Next steps
        1. Open in STM32CubeMX, configure clock and peripherals, generate MX_ init code.
        2. Copy MX_ generated files into Core/Src and Core/Inc.
        3. Implement TODO sections in each driver stub.
        4. Add gen_crc_frame.py / gen_uart_protocol.py outputs for communication.
        """)


