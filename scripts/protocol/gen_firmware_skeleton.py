def _arduino_sketch(project: str, peripherals: List[str]) -> str:
    setup_lines = ["Serial.begin(115200);", 'Serial.println("' + project + ' starting...");']
    if "spi" in peripherals:  setup_lines.append("SPI.begin();")
    if "i2c" in peripherals:  setup_lines.append("Wire.begin();")

    loop_lines = ["/* TODO: main loop */", "delay(10);"]

    setup_block = "\n    ".join(setup_lines)
    loop_block  = "\n    ".join(loop_lines)

    return dedent(f"""\
        /*
         * {project} — Arduino sketch
         * Auto-generated by gen_firmware_skeleton.py
         * Platform: Arduino (AVR/ESP32/STM32 via Arduino core)
         */
        {"#include <SPI.h>" if "spi" in peripherals else ""}
        {"#include <Wire.h>" if "i2c" in peripherals else ""}

        void setup() {{
            {setup_block}
        }}

        void loop() {{
            {loop_block}
        }}
        """).strip() + "\n"


def _arduino_readme(project: str) -> str:
    return dedent(f"""\
        # {project}

        Platform : Arduino
        Generated: gen_firmware_skeleton.py

        ## Build
        1. Open {project}.ino in the Arduino IDE (≥ 2.0)
        2. Select your board (Tools → Board)
        3. Upload (Ctrl+U)

        ## Next steps
        - Avoid `delay()` in production; use `millis()` for non-blocking timing.
        - For more complex logic, migrate to STM32CubeIDE or ESP-IDF.
        """)


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

@dataclass
class SkeletonOutput:
    success:         bool
    inputs:          Dict[str, Any]
    results:         Dict[str, Any]
    warnings:        List[str]
    recommendations: List[str]
    next_actions:    List[str]
    error:           Optional[Dict[str, str]] = None


def gen_firmware_skeleton(
    platform:     str,
    project_name: str           = "MyProject",
    series:       str           = "G4",
    peripherals:  List[str]     = None,
    rtos:         str           = "none",
) -> Dict[str, Any]:
    """
    Generate a firmware project skeleton.

    Parameters
    ----------
    platform     : "stm32" | "esp32" | "arduino"
    project_name : Project/module name (used in file names and guards)
    series       : STM32 series (F0/F1/F4/G0/G4/H7), ignored for other platforms
    peripherals  : List of peripheral names to include stubs for
    rtos         : "none" | "freertos" | "zephyr"
    """
    peripherals = [p.lower().strip() for p in (peripherals or [])]
    inputs: Dict[str, Any] = {
        "platform": platform, "project_name": project_name,
        "series": series, "peripherals": peripherals, "rtos": rtos,
    }

    try:
        plat = platform.lower().strip()
        if plat not in PLATFORMS:
            raise ValueError(f"Platform must be one of {PLATFORMS}; got '{platform}'")

        rtos_key = rtos.lower().strip()
        if rtos_key not in RTOS_OPTIONS:
            raise ValueError(f"RTOS must be one of {RTOS_OPTIONS}; got '{rtos}'")

        unknown_periph = [p for p in peripherals if p not in PERIPHERALS]
        if unknown_periph:
            raise ValueError(
                f"Unknown peripherals: {unknown_periph}. "
                f"Supported: {list(PERIPHERALS)}"
            )

        proj = project_name.strip().replace(" ", "_")
        files: Dict[str, str] = {}
        warnings:  List[str] = []
        recs:      List[str] = []

        if plat == "stm32":
            ser = series.upper().strip()
            if ser not in STM32_SERIES:
                raise ValueError(f"STM32 series must be one of {STM32_SERIES}; got '{series}'")

            files[f"{proj}/Core/Src/main.c"]      = _stm32_main(proj, ser, peripherals, rtos_key)
            files[f"{proj}/Core/Inc/main.h"]      = _stm32_main_h(proj, ser)
            files[f"{proj}/Makefile"]             = _stm32_makefile(proj, ser)
            files[f"{proj}/README.md"]            = _stm32_readme(proj, ser, peripherals, rtos_key)
            for p in peripherals:
                if p in ("uart","spi","i2c","tim","adc","dac","can"):
                    files[f"{proj}/Peripherals/{p}_driver.c"] = _stm32_peripheral_stub(p)

            if rtos_key == "freertos":
                recs.append(
                    "Add FreeRTOS source files from STM32CubeMX or copy from "
                    "github.com/FreeRTOS/FreeRTOS-Kernel into Middlewares/FreeRTOS/."
                )
            if "wifi" in peripherals or "ble" in peripherals:
                warnings.append(
                    "STM32 does not have built-in Wi-Fi/BLE. "
                    "Use an external module (ESP-AT, ESP32 co-processor, or Nordic nRF) "
                    "and communicate via UART or SPI."
                )

        elif plat == "esp32":
            files[f"{proj}/main/main.c"]                     = _esp32_main(proj, peripherals, rtos_key)
            files[f"{proj}/main/CMakeLists.txt"]             = _esp32_component_cmake(proj)
            files[f"{proj}/main/Kconfig.projbuild"]          = _esp32_kconfig(proj)
            files[f"{proj}/CMakeLists.txt"]                  = _esp32_cmakelists(proj)
            files[f"{proj}/README.md"]                       = _esp32_readme(proj, peripherals)

            if "can" in peripherals:
                warnings.append(
                    "ESP32 does not have a built-in CAN controller. "
                    "Use TWAI (Two-Wire Automotive Interface) with an external TJA1050 transceiver."
                )
            if "dac" in peripherals:
                recs.append(
                    "ESP32 has a built-in 8-bit DAC on GPIO25/GPIO26. "
                    "Use dac_output_enable() / dac_output_voltage() from driver/dac.h."
                )

        else:  # arduino
            files[f"{proj}/{proj}.ino"] = _arduino_sketch(proj, peripherals)
            files[f"{proj}/README.md"]  = _arduino_readme(proj)

            if rtos_key != "none":
                warnings.append(
                    "RTOS on Arduino is non-standard. "
                    "Consider migrating to ESP-IDF (ESP32) or STM32CubeIDE (STM32) for RTOS support."
                )

        # Common recommendations
        recs.append(
            "Integrate gen_uart_protocol.py output for custom serial communication "
            "by adding the generated .h/.c files to the project."
        )
        recs.append(
            "Add gen_crc_frame.py generated CRC function to a shared crc_utils.c/h."
        )
        recs.append(
            "Set up version control (git init) immediately and commit this skeleton "
            "as the initial commit before adding application logic."
        )

        dir_listing = sorted(files.keys())

        return asdict(SkeletonOutput(
            success=True, inputs=inputs,
            results={
                "files": files,
                "file_count": len(files),
                "directory_listing": dir_listing,
                "instructions": (
                    f"Create the directory '{proj}/' and write each file listed in "
                    "'files' at its given path. Then follow the README.md build steps."
                ),
            },
            warnings=warnings, recommendations=recs,
            next_actions=["gen_crc_frame", "gen_uart_protocol"],
        ))

    except Exception as exc:
        return asdict(SkeletonOutput(
            success=False, inputs=inputs, results={},
            warnings=[], recommendations=[], next_actions=[],
            error={"code": exc.__class__.__name__.upper(), "message": str(exc)},
        ))


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main() -> None:
    p = argparse.ArgumentParser(
        description="Firmware project skeleton generator",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument("--platform",   required=True, choices=PLATFORMS)
    p.add_argument("--project",    default="MyProject", dest="project_name")
    p.add_argument("--series",     default="G4",
                   help="STM32 series: F0/F1/F4/G0/G4/H7")
    p.add_argument("--peripherals", default="",
                   help="Comma-separated: uart,spi,i2c,tim,adc,dac,wifi,ble,can,usb")
    p.add_argument("--rtos",       default="none", choices=RTOS_OPTIONS)
    args = p.parse_args()

    periph_list = [x.strip() for x in args.peripherals.split(",") if x.strip()]
    result = gen_firmware_skeleton(
        platform=args.platform, project_name=args.project_name,
        series=args.series, peripherals=periph_list, rtos=args.rtos,
    )
    # Print without file contents to keep output readable in CLI
    out = dict(result)
    if "results" in out and "files" in out["results"]:
        out["results"] = dict(out["results"])
        out["results"]["files"] = {
            k: f"<{len(v)} chars>" for k, v in out["results"]["files"].items()
        }
    print(json.dumps(out, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
